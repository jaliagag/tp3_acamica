const suma = function (valor1, valor2){
    return valor1 + valor2;
}

const rest = function (valor1, valor2){
    return valor1 - valor2;
}

const multi = function (valor1, valor2){
    return valor1 * valor2;
}

const div = function (valor1, valor2){
    return valor1 + valor2;
}

module.exports = {
    suma,
    rest,
    multi,
    div
}